"""
Student Distilled model for emotion classification.

This model uses DistilRoBERTa with WavLM-Base and GRU party-tracker,
designed to be distilled from the Teacher model.
"""

import torch
import torch.nn as nn
import pytorch_lightning as pl
import torch.optim as optim
from transformers import AutoModel, WavLMModel
from sklearn.metrics import accuracy_score, f1_score
from typing import Optional, Dict
from pathlib import Path

from configs.base_config import BaseConfig

class StudentDistilledModel(pl.LightningModule):
    """
    Student Distilled model for emotion classification (LightningModule).
    
    Uses DistilRoBERTa, WavLM-Base, and GRU party-tracker.
    Multi-task learning for emotion and speaker (optional) classification.
    """
    
    def __init__(self, cfg: BaseConfig):
        """
        Initialize the model.
        
        Args:
            cfg: Configuration object (StudentConfig)
        """
        super().__init__()
        self.cfg = cfg
        
        hparams_to_save = {
            attr: getattr(cfg, attr) for attr in [
                'student_text_model_name', 'audio_encoder_model_name',
                'student_text_feature_dim', 'student_audio_feature_dim',
                'student_gru_hidden_dim', 'student_gru_layers', 'student_party_embed_dim',
                'output_dim', 'learning_rate', 'input_mode', 'dataset_name',
                'num_speakers_for_student_model',
                'train_speaker_classifier', 'speaker_loss_weight',
                'student_classifier_dropout'
            ] if hasattr(cfg, attr)
        }
        self.save_hyperparameters(hparams_to_save)
        
        # Text Encoder (DistilRoBERTa)
        self.text_encoder = AutoModel.from_pretrained(self.cfg.student_text_model_name)
        
        # Audio Encoder (WavLM)
        self.audio_encoder = WavLMModel.from_pretrained(self.cfg.audio_encoder_model_name)
        
        # Speaker/Party Embedding
        self.num_speakers = getattr(self.cfg, 'num_speakers_for_student_model', 20)
        self.speaker_embedding = nn.Embedding(
            num_embeddings=self.num_speakers,
            embedding_dim=self.cfg.student_party_embed_dim
        )
        
        # GRU Party Tracker
        gru_input_dim = (self.cfg.student_text_feature_dim +
                         self.cfg.student_audio_feature_dim +
                         self.cfg.student_party_embed_dim)
        
        self.party_tracker = nn.GRU(
            input_size=gru_input_dim,
            hidden_size=self.cfg.student_gru_hidden_dim,
            num_layers=self.cfg.student_gru_layers,
            batch_first=True
        )
        
        fusion_dim = (self.cfg.student_text_feature_dim +
                      self.cfg.student_audio_feature_dim +
                      self.cfg.student_gru_hidden_dim)
        
        dropout_rate = getattr(self.cfg, 'student_classifier_dropout', 0.3)
        self.emotion_classifier = nn.Sequential(
            nn.Linear(fusion_dim, self.cfg.student_gru_hidden_dim),
            nn.ReLU(),
            nn.Dropout(dropout_rate),
            nn.Linear(self.cfg.student_gru_hidden_dim, self.cfg.output_dim)
        )
        
        self.speaker_classifier = None
        if getattr(self.cfg, 'train_speaker_classifier', False):
            self.speaker_classifier = nn.Sequential(
                nn.Linear(fusion_dim, self.cfg.student_gru_hidden_dim),
                nn.ReLU(),
                nn.Dropout(dropout_rate),
                nn.Linear(self.cfg.student_gru_hidden_dim, self.num_speakers)
            )
            self.speaker_criterion = nn.CrossEntropyLoss()
        
        self.emotion_criterion = nn.CrossEntropyLoss()
    
        self._report_model_info_on_init()
    
    def _report_model_info_on_init(self):
        trainable_params = sum(p.numel() for p in self.parameters() if p.requires_grad)
        total_params = sum(p.numel() for p in self.parameters())
        print(f"Initialized StudentDistilledModel (LightningModule):")
        print(f"  Text Encoder: {self.cfg.student_text_model_name}")
        print(f"  Audio Encoder: {self.cfg.audio_encoder_model_name}")
        print(f"  GRU Hidden Dim: {self.cfg.student_gru_hidden_dim}, Layers: {self.cfg.student_gru_layers}")
        print(f"  Emotion Classes: {self.cfg.output_dim}")
        if self.speaker_classifier:
            print(f"  Speaker Classifier Enabled: {self.num_speakers} speakers")
        print(f"  Trainable Parameters: {trainable_params:,} / {total_params:,} ({trainable_params/total_params:.2%})")
    
    def forward(self, batch: Dict[str, torch.Tensor]):
        text_outputs = self.text_encoder(
            input_ids=batch['text_input_ids'],
            attention_mask=batch.get('text_attention_mask')
        )
        text_features = text_outputs.last_hidden_state.mean(dim=1)
        
        audio_outputs = self.audio_encoder(
            input_values=batch['audio_input_values'],
            attention_mask=batch.get('audio_attention_mask')
        )
        audio_features = audio_outputs.last_hidden_state.mean(dim=1)
        
        speaker_ids = batch.get('speaker_ids')
        if speaker_ids is not None:
            speaker_ids_clamped = torch.clamp(speaker_ids, 0, self.num_speakers - 1)
            speaker_features = self.speaker_embedding(speaker_ids_clamped)
        else:
            batch_size = text_features.size(0)
            speaker_features = torch.zeros(
                batch_size, self.cfg.student_party_embed_dim, device=text_features.device
            )
        
        gru_input = torch.cat([text_features, audio_features, speaker_features], dim=1)
        
        if gru_input.dim() == 2:
            gru_input = gru_input.unsqueeze(1)
        
        gru_output, _ = self.party_tracker(gru_input)
        gru_features = gru_output[:, -1, :]
        
        fused_features = torch.cat([text_features, audio_features, gru_features], dim=1)
        
        emotion_logits = self.emotion_classifier(fused_features)
        
        speaker_logits = None
        if self.speaker_classifier is not None:
            speaker_logits = self.speaker_classifier(fused_features)
        
        return {
            'emotion_logits': emotion_logits,
            'speaker_logits': speaker_logits
        }

    def _shared_step(self, batch: Dict[str, torch.Tensor]):
        outputs = self(batch)
        emotion_logits = outputs['emotion_logits']
        speaker_logits = outputs.get('speaker_logits')

        emotion_labels = batch.get('labels', batch.get('label'))
        if emotion_labels is None:
            raise KeyError("Batch must contain 'labels' or 'label' for emotion ground truth.")

        total_loss = self.emotion_criterion(emotion_logits, emotion_labels)
        emotion_preds = torch.argmax(emotion_logits, dim=1)

        speaker_loss_val = torch.tensor(0.0, device=total_loss.device)
        if self.speaker_classifier is not None and speaker_logits is not None:
            speaker_labels = batch.get('speaker_ids')
            if speaker_labels is not None:
                speaker_labels_clamped = torch.clamp(speaker_labels, 0, self.num_speakers - 1)
                current_speaker_loss = self.speaker_criterion(speaker_logits, speaker_labels_clamped)
                total_loss += getattr(self.cfg, 'speaker_loss_weight', 0.5) * current_speaker_loss
                speaker_loss_val = current_speaker_loss

        return total_loss, emotion_preds, emotion_labels, speaker_loss_val

    def training_step(self, batch: Dict[str, torch.Tensor], batch_idx: int):
        loss, emotion_preds, emotion_labels, speaker_loss = self._shared_step(batch)
        
        labels_cpu = emotion_labels.cpu().numpy()
        preds_cpu = emotion_preds.cpu().numpy()
        acc = accuracy_score(labels_cpu, preds_cpu)
        f1 = f1_score(labels_cpu, preds_cpu, average='weighted', zero_division=0)
        
        self.log('train_loss', loss, on_step=True, on_epoch=True, prog_bar=True, logger=True)
        self.log('train_emotion_acc', acc, on_step=False, on_epoch=True, prog_bar=True, logger=True)
        self.log('train_emotion_f1', f1, on_step=False, on_epoch=True, prog_bar=True, logger=True)
        if self.speaker_classifier and speaker_loss.item() > 0:
            self.log('train_speaker_loss', speaker_loss, on_step=True, on_epoch=True, logger=True)
        return loss

    def validation_step(self, batch: Dict[str, torch.Tensor], batch_idx: int):
        loss, emotion_preds, emotion_labels, speaker_loss = self._shared_step(batch)
        labels_cpu = emotion_labels.cpu().numpy()
        preds_cpu = emotion_preds.cpu().numpy()
        acc = accuracy_score(labels_cpu, preds_cpu)
        f1 = f1_score(labels_cpu, preds_cpu, average='weighted', zero_division=0)
        
        self.log('val_loss', loss, on_step=False, on_epoch=True, prog_bar=True, logger=True)
        self.log('val_emotion_acc', acc, on_step=False, on_epoch=True, prog_bar=True, logger=True)
        self.log('val_emotion_f1', f1, on_step=False, on_epoch=True, prog_bar=True, logger=True)
        self.log('val_wf1', f1, on_step=False, on_epoch=True, prog_bar=False, logger=True)
        if self.speaker_classifier and speaker_loss.item() > 0:
             self.log('val_speaker_loss', speaker_loss, on_step=False, on_epoch=True, logger=True)
        return loss

    def test_step(self, batch: Dict[str, torch.Tensor], batch_idx: int):
        loss, emotion_preds, emotion_labels, speaker_loss = self._shared_step(batch)
        labels_cpu = emotion_labels.cpu().numpy()
        preds_cpu = emotion_preds.cpu().numpy()
        acc = accuracy_score(labels_cpu, preds_cpu)
        f1 = f1_score(labels_cpu, preds_cpu, average='weighted', zero_division=0)
        
        self.log('test_loss', loss, on_step=False, on_epoch=True, logger=True)
        self.log('test_emotion_acc', acc, on_step=False, on_epoch=True, logger=True)
        self.log('test_emotion_f1', f1, on_step=False, on_epoch=True, logger=True)
        if self.speaker_classifier and speaker_loss.item() > 0:
            self.log('test_speaker_loss', speaker_loss, on_step=False, on_epoch=True, logger=True)
        return loss

    def configure_optimizers(self):
        optimizer = optim.AdamW(
            filter(lambda p: p.requires_grad, self.parameters()),
            lr=self.cfg.learning_rate
        )
        return optimizer


